// In Javascript, we have 7 type of operators
let x = 10;
let y = 15;

//Arithmetic Operator
// Here x+y is a expression, cause it generate some value
console.log(x + y);

//Increment/Decrement Operator
console.log(++x);
console.log(y--);

x += 20;

//Relational Operator
console.log(x > y);

//Equality Operator

// Check TYPE + VALUE
console.log(x === y);

console.log(x !== y);

//Check Value Only
//Convert right operand data type to left operand data type while compilation
console.log('1' == 1);

//Conditional/Ternary Operator
let marks = 50;
//          condition    if true      else
let result = marks > 40 ? 'Passed' : 'Failed'

